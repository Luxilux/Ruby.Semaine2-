def echo(string)
  string
end
# -----------------------------------------------------------------------------
def shout(string)
  string.upcase
end
# -----------------------------------------------------------------------------
def repeat(string, n=2)
  n.times.collect { string }.join(' ')
end
# -----------------------------------------------------------------------------
def start_of_word(string, n)
  string[0]
  string[1]
  string[0..(n-1)]
end
# -----------------------------------------------------------------------------
def first_word(string)
  string.split(" ")[0]
end
# -----------------------------------------------------------------------------
def titleize(string)
  string.capitalize
  string.each.capitalize
end
